    //CARGAR TABLA
    private void cargarTabla() {
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(
            new Object[]{"Código", "Título", "Autor"}, 0
    );

         // libroDAO.listarLibros() ahora devuelve List<Libro>
    for (Libro l : libroDAO.listarLibros()) {
        modelo.addRow(new Object[]{
            l.getId(),      // String con el código (lo construimos así en LibroDAO)
            l.getTitulo(),
            l.getAutor()
        });
    }
    tablaLibros.setModel(modelo);
}
    
    
    //AGREGAR
    private void agregar() {
        try {
            int codigo = Integer.parseInt(txtID.getText());
            String titulo = txtTitulo.getText();
            String autor = txtAutor.getText();

            libroDAO.insertarLibro(codigo, titulo, autor);
            cargarTabla();
            limpiar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR al agregar: " + e.getMessage());
        }
    }
    
    
    //EDITAR
    private void editar() {
        try {
            int codigo = Integer.parseInt(txtID.getText());

            // Verificar si existe antes de editar
            if (libroDAO.buscarPorCodigo(codigo) == null) {
                JOptionPane.showMessageDialog(this, "No existe un libro con ese código");
                return;
            }

            libroDAO.actualizarLibro(codigo, txtTitulo.getText(), txtAutor.getText());
            cargarTabla();
            limpiar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR al actualizar: " + e.getMessage());
        }
    }
    
    
    //ELIMINAR
    private void eliminar() {
        try {
            int codigo = Integer.parseInt(txtID.getText());

            if (libroDAO.buscarPorCodigo(codigo) == null) {
                JOptionPane.showMessageDialog(this, "El libro no existe.");
                return;
            }

            libroDAO.eliminarLibro(codigo);
            cargarTabla();
            limpiar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR al eliminar: " + e.getMessage());
        }
    }
    
    
      //LIMPIAR CAMPOS
    private void limpiar() {
        txtID.setText("");
        txtTitulo.setText("");
        txtAutor.setText("");
        txtID.requestFocus();
    }
                                        

