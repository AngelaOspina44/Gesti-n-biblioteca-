    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
                                          
    int fila = tablaLibros.getSelectedRow();

    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione un libro en la tabla");
        return;
    }

    try {
        int codigo = Integer.parseInt(tablaLibros.getValueAt(fila, 0).toString());
        String nuevoTitulo = txtTitulo.getText();
        String nuevoAutor = txtAutor.getText();

        if (nuevoTitulo.isEmpty() || nuevoAutor.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe llenar título y autor");
            return;
        }

        libroDAO.actualizarLibro(codigo, nuevoTitulo, nuevoAutor);

        cargarTabla();
        btnLimpiarActionPerformed(null);

        JOptionPane.showMessageDialog(this, "Libro actualizado correctamente");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al editar: " + e.getMessage());
    }

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
                                           
    int fila = tablaLibros.getSelectedRow();

    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione un libro en la tabla");
        return;
    }

    try {
        // Obtener código desde la tabla (columna 0)
        int codigo = Integer.parseInt(tablaLibros.getValueAt(fila, 0).toString());

        // Verificar existencia
        if (libroDAO.buscarPorCodigo(codigo) == null) {
            JOptionPane.showMessageDialog(this, "El libro no existe.");
            return;
        }

        // Eliminar
        libroDAO.eliminarLibro(codigo);

        // Refrescar tabla
        cargarTabla();

        // Limpiar campos
        txtID.setText("");
        txtTitulo.setText("");
        txtAutor.setText("");
        tablaLibros.clearSelection();

        JOptionPane.showMessageDialog(this, "Libro eliminado correctamente");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage());
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

