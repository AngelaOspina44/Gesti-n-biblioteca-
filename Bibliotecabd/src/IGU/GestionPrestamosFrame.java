    private String formatearFecha(Date fecha) {
    if (fecha == null) return "";
    // Convertimos a LocalDate en UTC para evitar desfases de zona horaria:
    // Primero obtener instant y representarlo en UTC, luego a LocalDate y formatear.
    java.time.LocalDate ld = Instant.ofEpochMilli(fecha.getTime()).atZone(ZoneId.of("UTC")).toLocalDate();
    return ld.toString(); // yyyy-MM-dd
}

