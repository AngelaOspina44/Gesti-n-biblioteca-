    private void cargarSuscriptores() {
        comboSuscriptor.removeAllItems();
        List<Suscriptor> lista = suscriptorDAO.obtenerTodos();
        
        for (Suscriptor s : lista) {
            comboSuscriptor.addItem(s.getDocumento() + " - " + s.getNombre());
        }
    }


    private void cargarLibros() {
        cmbLibros.removeAllItems();
        for(Libro l : libroDAO.obtenerTodos()){
            cmbLibros.addItem(l.getId() + " - " + l.getTitulo());
        }
    }

    private void cargarTablaPrestamos() {
    DefaultTableModel modelo = (DefaultTableModel) tablaPrestamos.getModel();
    // Asegúrate de que la tabla tenga 6 columnas: ID, Suscriptor, Libro, Fecha Prestamo, Fecha Devolucion, Devuelto
    modelo.setRowCount(0);

    for (Prestamo p : prestamoDAO.obtenerTodos()) {
        Suscriptor s = suscriptorDAO.buscarPorId(p.getIdSuscriptor());
        Libro l = libroDAO.buscarPorId(p.getIdLibro());

        modelo.addRow(new Object[]{
            p.getId(),
            (s != null ? s.getNombre() : "DESCONOCIDO"),
            (l != null ? l.getTitulo() : "DESCONOCIDO"),
            formatearFecha(p.getFechaPrestamo()),
            formatearFecha(p.getFechaDevolucion()),
            p.isDevuelto() ? "Sí" : "No"
        });
    }
}

    private void registrarPrestamo() {

    if (comboSuscriptor.getSelectedItem() == null || cmbLibros.getSelectedItem() == null) {
        JOptionPane.showMessageDialog(this, "Seleccione suscriptor y libro");
        return;
    }

    // Obtener documento del suscriptor
    String documentoSuscriptor = comboSuscriptor.getSelectedItem().toString().split(" - ")[0];

    // Obtener código del libro
    String idLibro = cmbLibros.getSelectedItem().toString().split(" - ")[0];

    // Verificar si el libro está prestado
    if (prestamoDAO.libroEstaPrestado(idLibro)) {
        JOptionPane.showMessageDialog(this, "❌ Ese libro ya está prestado");
        return;
    }

    // Crear objeto Prestamo
    Prestamo p = new Prestamo();
    p.setIdUsuario("admin");
    p.setIdSuscriptor(documentoSuscriptor);
    p.setIdLibro(idLibro);
    p.setFechaPrestamo(new Date());
    p.setFechaDevolucion(null);
    p.setDevuelto(false);

    // Guardar en Mongo
    if (prestamoDAO.registrarPrestamo(p)) {
        JOptionPane.showMessageDialog(this, "✔ Préstamo registrado con éxito");
        cargarTablaPrestamos();
    } else {
        JOptionPane.showMessageDialog(this, "❌ Error registrando el préstamo");
    }
}

    private void devolverPrestamo() {
    int fila = tablaPrestamos.getSelectedRow();
    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione un préstamo");
        return;
    }

    String idPrestamo = tablaPrestamos.getValueAt(fila, 0).toString();

    boolean exito = prestamoDAO.marcarComoDevuelto(idPrestamo);

    if (exito) {
        JOptionPane.showMessageDialog(this, "Préstamo marcado como devuelto.");
        cargarTablaPrestamos();
    } else {
        JOptionPane.showMessageDialog(this, "No se pudo devolver el préstamo.");
    }
}


