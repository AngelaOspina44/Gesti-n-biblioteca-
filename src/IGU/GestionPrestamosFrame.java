   // ========================= CARGAR USUARIOS =============================
    private void cargarUsuarios() {
        cmbUsuarios.removeAllItems();
        for(Usuario u : usuarioDAO.obtenerTodos()){
            cmbUsuarios.addItem(u.getId() + " - " + u.getNombre());
        }
    }

    // ========================= CARGAR LIBROS ===============================
    private void cargarLibros() {
        cmbLibros.removeAllItems();
        for(Libro l : libroDAO.obtenerTodos()){
            cmbLibros.addItem(l.getId() + " - " + l.getTitulo());
        }
    }

    // ========================= REGISTRAR PRESTAMO ==========================
    private void registrarPrestamo(){

        if(cmbUsuarios.getSelectedItem()==null || cmbLibros.getSelectedItem()==null){
            JOptionPane.showMessageDialog(this,"Seleccione usuario y libro");
            return;
        }

        String idUsuario = cmbUsuarios.getSelectedItem().toString().split(" - ")[0];
        String idLibro   = cmbLibros.getSelectedItem().toString().split(" - ")[0];

        if(prestamoDAO.libroEstaPrestado(idLibro)){
            JOptionPane.showMessageDialog(this,"‚ùå Ese libro ya est√° prestado");
            return;
        }

        Prestamo p = new Prestamo(null,idUsuario,idLibro,txtFechaPrestamo.getText(),"",false);

        if(prestamoDAO.registrarPrestamo(p)){
            JOptionPane.showMessageDialog(this,"‚úî Pr√©stamo registrado");
            cargarTablaPrestamos();
        }
    }

    // ========================= DEVOLVER PRESTAMO ===========================
    private void devolverPrestamo(){

        int fila = tablaPrestamos.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(this,"Seleccione un pr√©stamo de la tabla");
            return;
        }

        String idPrestamo = tablaPrestamos.getValueAt(fila,0).toString();

        if(prestamoDAO.marcarComoDevuelto(idPrestamo)){
            JOptionPane.showMessageDialog(this,"üìó Pr√©stamo devuelto");
            cargarTablaPrestamos();
        }
    }

    // ========================= CARGAR TABLA ================================
    private void cargarTablaPrestamos(){

        DefaultTableModel modelo = new DefaultTableModel();
    modelo.setColumnIdentifiers(new String[]{"ID", "Usuario", "Libro", "Fecha Pr√©stamo", "Devuelto"});

    for (Prestamo p : prestamoDAO.obtenerTodos()) {

        // usuario
        Usuario u = usuarioDAO.buscarPorId(p.getIdUsuario());
        String nomUsuario = (u != null) ? u.getNombre() : "DESCONOCIDO";

        // libro
        Libro l = libroDAO.buscarPorId(p.getIdLibro());
        String nomLibro = (l != null) ? l.getTitulo() : "DESCONOCIDO";

        modelo.addRow(new Object[]{
            p.getId(),
            nomUsuario,
            nomLibro,
            p.getFechaPrestamo(),
            p.isDevuelto() ? "S√≠" : "No"
        });
    }

    tablaPrestamos.setModel(modelo);

}

